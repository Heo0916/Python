# 1. 대입 연산
#    1) = 등호 왼쪽(변수), 등호 오른쪽(값)
#       10 = 정수 : 불가능한 형태
#    2) += 등호 오른쪽 값을 등호 왼쪽에 더해 준다.
#    3) -=
#    4) *=
#    5) /=
정수 = 100
#정수 = 정수 + 50

정수 += 50 # 정수 = 정수 + 50
print(정수)

# 저금통에 저금하기 시뮬레이션
